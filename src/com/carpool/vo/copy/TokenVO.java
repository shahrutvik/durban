package com.carpool.vo.copy;

public class TokenVO {
	
	private int idToken;
	private int idEvent;
	private int idMemberGroup;
	private int earnedToken;
	
	public int getIdToken() {
		return idToken;
	}
	public void setIdToken(int idToken) {
		this.idToken = idToken;
	}
	public int getIdMemberGroup() {
		return idMemberGroup;
	}
	public void setIdMemberGroup(int idMemberGroup) {
		this.idMemberGroup = idMemberGroup;
	}
	public int getEarnedToken() {
		return earnedToken;
	}
	public void setEarnedToken(int earnedToken) {
		this.earnedToken = earnedToken;
	}
	public int getIdEvent() {
		return idEvent;
	}
	public void setIdEvent(int idEvent) {
		this.idEvent = idEvent;
	}
	

}
